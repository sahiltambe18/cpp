     cout<<"a<=b :"<<(a<=b)<<endl;
     cout<<"a>=b :"<<(a>=b)<<endl;
     cout<<"a<b :"<<(a<b)<<endl;
     cout<<"a>b :"<<(a>b)<<endl;
    //  logical operators
    cout<<"follow are logical operators"<<endl;
     cout<<"((a==b)and(a!=b)): "<<((a==b)&&(a!=b))<<endl;
